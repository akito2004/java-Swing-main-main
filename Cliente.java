    public class Cliente {          // Define una clase interna Cliente para almacenar los datos del cliente.
        String nombre;              // Variable para almacenar el nombre del cliente.
        String direccion;           // Variable para almacenar la dirección del cliente.
        String telefono;            // Variable para almacenar el número de teléfono del cliente.

        Cliente(String nombre, String direccion, String telefono) {  // Constructor de la clase Cliente.
            this.nombre = nombre;      // Asigna el nombre del cliente.
            this.direccion = direccion;// Asigna la dirección del cliente.
            this.telefono = telefono;  // Asigna el teléfono del cliente.
        }
    }

    
